    public ShowLOG() {
        initComponents();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String log = open("LOG.bakchod");
        int sno = 0;
        String f[] = log.split("error");
        log = "";
        for (int d = 1; d < f.length; ++d) {
            log = log + f[d];
            if (d == f.length - 1) {
            } else {
                log = log + "error";
            }
        }
        for (String str : log.split("\n")) {
            int k = 0;
            String from = "", time = "", msg = "";

            for (String s : str.split("\t")) {
                switch (k) {
                    case 0:
                        from = s;
                        break;
                    case 1:
                        time = s;
                        break;
                    case 2:
                        msg = s;
                        break;
                    default:
                        k = 0;
                }
                ++k;
            }
            if (sno == 0) {
            } else {
                model.addRow(new Object[]{sno, "" + from, "" + time, "" + msg});
            }
            sno++;
        }
    }

