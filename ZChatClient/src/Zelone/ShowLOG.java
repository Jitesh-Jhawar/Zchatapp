  private String open(String path) {
        String cc;
        try {
            File f = new File(path);
            Scanner input = new Scanner(f);
            String kkkk = "";
            while (input.hasNextLine()) {
                kkkk = kkkk + "\n" + (String) input.nextLine();
            }
            cc = kkkk;
        } catch (Exception e) {
            cc = "error";
        }
        return "" + cc;
    }

